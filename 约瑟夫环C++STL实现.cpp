void Josephus(int total,int begin,int stepNum){
	for(int i=1;i<=total;i++){
		student.push_back(i);
	}
	int track=begin-2+stepNum;
	while(!student.empty()){
		if(track>=student.size())track=track%student.size();
		cout<<student.at(track)<<endl;
		student.erase(student.begin()+track);
		track+=stepNum-1;
	}
}
	 
